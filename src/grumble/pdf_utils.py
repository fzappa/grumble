import pdfplumber
import streamlit as st

def extract_text_from_pdf(file):
    text = ""
    try:
        with pdfplumber.open(file) as pdf:
            for page in pdf.pages:
                text += page.extract_text() or ""
    except Exception as e:
        st.error(f"Failed to extract text from PDF: {str(e)}")
    return text

